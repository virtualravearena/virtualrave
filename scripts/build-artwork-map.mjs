import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const sourcePath = path.resolve("data/assets.grove.json");
const outputPath = path.resolve("lib/vr303Artwork.ts");

const raw = JSON.parse(readFileSync(sourcePath, "utf8"));
if (!Array.isArray(raw.tokens)) {
  throw new Error(`Expected tokens[] in ${sourcePath}`);
}

const entries = [];
for (const t of raw.tokens) {
  const id = Number(t.tokenId);
  const url = t?.image?.gatewayUrl ?? t?.image?.lensUri;
  if (!Number.isInteger(id) || id < 1 || id > 303) {
    throw new Error(`Bad tokenId: ${t.tokenId}`);
  }
  if (typeof url !== "string" || !url.startsWith("https://")) {
    throw new Error(`Bad URL for token ${id}: ${url}`);
  }
  entries.push([id, url]);
}
entries.sort((a, b) => a[0] - b[0]);

const seen = new Set();
for (const [id] of entries) {
  if (seen.has(id)) throw new Error(`Duplicate tokenId in source: ${id}`);
  seen.add(id);
}

const body = entries.map(([id, url]) => `  ${id}: ${JSON.stringify(url)},`).join("\n");

const out = `// AUTO-GENERATED by scripts/build-artwork-map.mjs. Do not edit by hand.
// Source: data/assets.grove.json (${entries.length} tokens)

export const VR303_TOKEN_IMAGES: Readonly<Record<number, string>> = {
${body}
};

export function getVr303Artwork(tokenId: number): string | undefined {
  return VR303_TOKEN_IMAGES[tokenId + 1];
}
`;

writeFileSync(outputPath, out, "utf8");
console.log(`Wrote ${entries.length} entries to ${outputPath}`);
